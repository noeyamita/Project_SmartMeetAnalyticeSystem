//renderRooms() - ‡πÅ‡∏™‡∏î‡∏á‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏´‡πâ‡∏≠‡∏á‡∏õ‡∏£‡∏∞‡∏ä‡∏∏‡∏° (‡∏£‡∏π‡∏õ‡∏Ç‡∏∂‡πâ‡∏ô‡πÅ‡∏ô‡πà‡∏ô‡∏≠‡∏ô)
function renderRooms(rooms) {
    const roomsGrid = document.getElementById('roomsGrid');
    roomsGrid.innerHTML = '';

    if (!rooms || rooms.length === 0) {
        roomsGrid.innerHTML = '<div class="empty-state">‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏´‡πâ‡∏≠‡∏á‡∏õ‡∏£‡∏∞‡∏ä‡∏∏‡∏°‡πÉ‡∏ô‡∏ä‡πà‡∏ß‡∏á‡πÄ‡∏ß‡∏•‡∏≤‡∏ó‡∏µ‡πà‡∏Å‡∏≥‡∏´‡∏ô‡∏î</div>';
        return;
    }

    rooms.forEach(room => {
        const card = document.createElement('div');

        //‡πÉ‡∏ä‡πâ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏à‡∏≤‡∏Å API
        const status = room.availability_status || 'unknown';
        const statusText = room.availability_text || '‡πÑ‡∏°‡πà‡∏ó‡∏£‡∏≤‡∏ö‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞';

        //‡∏™‡∏µ‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞
        let chipClass = '';
        let statusClass = '';
        if (status === 'available') {
            chipClass = 'available';
            statusClass = 'available';
        } else if (status === 'booked') {
            chipClass = 'booked';
            statusClass = 'unavailable';
        } else if (status === 'closed') {
            chipClass = 'closed';
            statusClass = 'unavailable';
        } else {
            chipClass = 'unknown';
            statusClass = 'unavailable';
        }

        //‡∏´‡πâ‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏Å‡∏î‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÑ‡∏î‡πâ
        const isAvailable = status === 'available';

        card.className = `room-card ${statusClass}`;
        card.setAttribute('data-room-id', room.room_id);

        //‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö path ‡∏£‡∏π‡∏õ (‡πÑ‡∏°‡πà‡πÄ‡∏ï‡∏¥‡∏° ../html/)
        const imageUrl = room.image_url && room.image_url.trim() !== ''
            ? room.image_url
            : 'uploads/rooms/default_room.jpg';

        const operatingHours = `${room.open_time || '00:00'} - ${room.close_time || '23:59'}`;
        const roomLocation = `${room.floor_number || '-'} | ‡∏Ç‡∏ô‡∏≤‡∏î ${room.room_size || 'N/A'}`;

        //‡∏™‡∏£‡πâ‡∏≤‡∏á HTML ‡∏Å‡∏≤‡∏£‡πå‡∏î
        card.innerHTML = `
            <div class="room-image" style="background-image: url('${imageUrl}');"></div>
            <div class="room-details">
                <div class="room-title">${room.room_name}</div>
                <div class="operating-hours">${operatingHours}</div>
                <div class="room-cap">‡∏Ñ‡∏ß‡∏≤‡∏°‡∏à‡∏∏ ${room.capacity} ‡∏Ñ‡∏ô<br>${roomLocation}</div>
                <div class="room-status">
                    <div class="status-badge">
                        <span class="chip ${chipClass}"></span>
                        ${statusText}
                    </div>
                    <button class="btn primary" onclick="openBookingModal(${room.room_id})" ${isAvailable ? '' : 'disabled'}>
                        ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏´‡πâ‡∏≠‡∏á
                    </button>
                </div>
            </div>
        `;
        roomsGrid.appendChild(card);
    });
}



// 6. openBookingModal() - ‡πÄ‡∏õ‡∏¥‡∏î Modal ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏à‡∏≠‡∏á
function openBookingModal(roomId) {
    selectedRoom = availableRooms.find(room => room.room_id == roomId);

    if (!selectedRoom) {
        showAlert('‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡πâ‡∏≠‡∏á‡∏õ‡∏£‡∏∞‡∏ä‡∏∏‡∏°‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å');
        return;
    }

    const modalRoomInfo = document.getElementById('modalRoomInfo');
    const openTime = document.getElementById('start_time').value;
    const closeTime = document.getElementById('end_time').value;
    const date = document.getElementById('date').value;
    const capacity = document.getElementById('capacity').value;

    //‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏Ñ‡∏ô‡πÑ‡∏°‡πà‡πÄ‡∏Å‡∏¥‡∏ô‡∏Ñ‡∏ß‡∏≤‡∏°‡∏à‡∏∏‡∏Ç‡∏≠‡∏á‡∏´‡πâ‡∏≠‡∏á
    if (capacity > selectedRoom.capacity) {
        showAlert(`‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ú‡∏π‡πâ‡πÄ‡∏Ç‡πâ‡∏≤‡∏£‡πà‡∏ß‡∏° (${capacity} ‡∏Ñ‡∏ô) ‡πÄ‡∏Å‡∏¥‡∏ô‡∏Ñ‡∏ß‡∏≤‡∏°‡∏à‡∏∏‡∏Ç‡∏≠‡∏á‡∏´‡πâ‡∏≠‡∏á ${selectedRoom.room_name} (‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡πÑ‡∏î‡πâ ${selectedRoom.capacity} ‡∏Ñ‡∏ô)`, 'error', 5000);
        return;
    }

    // ‡πÅ‡∏õ‡∏•‡∏á‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡πÑ‡∏ó‡∏¢
    const dateObj = new Date(date + 'T00:00:00');
    const thaiDate = dateObj.toLocaleDateString('th-TH', { 
        year: 'numeric', 
        month: 'long', 
        day: 'numeric' 
    });

    modalRoomInfo.innerHTML = `
        ‡∏´‡πâ‡∏≠‡∏á <strong>${selectedRoom.room_name}</strong> | 
        ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô ${capacity} ‡∏Ñ‡∏ô | 
        ‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà ${thaiDate}<br>
        ‡πÄ‡∏ß‡∏•‡∏≤ <strong>${openTime} - ${closeTime}</strong>
    `;

    document.getElementById('bookingModal').classList.add('active');
}

